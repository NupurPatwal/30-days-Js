// Declare variables of different data types
let name = "John Doe"; // String
let age = 30; // Number
let isStudent = true; // Boolean
let hobbies = ["reading", "gaming", "coding"]; // Array
let address = { city: "New York", country: "USA" }; // Object
let undefinedVar; // Undefined
let nullVar = null; // Null
let bigIntVar = BigInt(12345678901234567890); // BigInt
let symbolVar = Symbol("unique"); // Symbol

// Log the value and type of each variable to the console
console.log(`name: ${name}, type: ${typeof name}`);
console.log(`age: ${age}, type: ${typeof age}`);
console.log(`isStudent: ${isStudent}, type: ${typeof isStudent}`);
console.log(`hobbies: ${hobbies}, type: ${typeof hobbies}`);
console.log(`address: ${address}, type: ${typeof address}`);
console.log(`undefinedVar: ${undefinedVar}, type: ${typeof undefinedVar}`);
console.log(`nullVar: ${nullVar}, type: ${typeof nullVar}`);
console.log(`bigIntVar: ${bigIntVar}, type: ${typeof bigIntVar}`);
